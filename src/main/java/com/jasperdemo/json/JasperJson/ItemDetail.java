package com.jasperdemo.json.JasperJson;

public class ItemDetail {

    String silItemName;
    String measurementUnit;
    String pricePerUnit;
    String quantity;
    String silItemCategory;
    String silItemType;
    String remarks;

    public String getSilItemName() {
        return silItemName;
    }

    public void setSilItemName(String silItemName) {
        this.silItemName = silItemName;
    }

    public String getMeasurementUnit() {
        return measurementUnit;
    }

    public void setMeasurementUnit(String measurementUnit) {
        this.measurementUnit = measurementUnit;
    }

    public String getPricePerUnit() {
        return pricePerUnit;
    }

    public void setPricePerUnit(String pricePerUnit) {
        this.pricePerUnit = pricePerUnit;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

    public String getSilItemCategory() {
        return silItemCategory;
    }

    public void setSilItemCategory(String silItemCategory) {
        this.silItemCategory = silItemCategory;
    }

    public String getSilItemType() {
        return silItemType;
    }

    public void setSilItemType(String silItemType) {
        this.silItemType = silItemType;
    }

    public String getRemarks() {
        return remarks;
    }

    public void setRemarks(String remarks) {
        this.remarks = remarks;
    }
}
